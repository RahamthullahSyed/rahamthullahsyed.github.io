// Initialize Portfolio Website
document.addEventListener("DOMContentLoaded", () => {
  const appsGrid = document.getElementById("apps-grid");
  const tabBtns = document.querySelectorAll(".tab-btn");
  const themeToggle = document.getElementById("theme-toggle");

  let activeCategory = "all";

  // Render Apps list
  function renderApps(category = "all") {
    appsGrid.innerHTML = "";
    const filteredApps = category === "all" 
      ? appsData 
      : appsData.filter(app => app.category === category);

    if (filteredApps.length === 0) {
      appsGrid.innerHTML = `
        <div class="col-span-full text-center py-16 text-gray-500">
          <p class="text-lg font-medium">No apps found in this category.</p>
        </div>
      `;
      return;
    }

    filteredApps.forEach(app => {
      const card = document.createElement("div");
      card.className = "app-card group opacity-0 translate-y-4";
      card.style.animation = "fadeInUp 0.6s forwards cubic-bezier(0.16, 1, 0.3, 1)";
      
      const badgeHtml = app.badge 
        ? `<span class="app-badge bg-gradient-to-r ${app.color} text-white">${app.badge}</span>` 
        : "";

      // Feature graphic banner HTML
      const bannerHtml = app.featureGraphicUrl 
        ? `<div class="app-card-banner" style="background-image: url('${app.featureGraphicUrl}');"></div>`
        : `<div class="app-card-banner bg-gradient-to-br ${app.color}"></div>`;

      card.innerHTML = `
        ${bannerHtml}
        <div class="app-card-inner-new">
          <div class="app-card-header-new">
            <img class="app-icon-new" src="${app.iconUrl}" alt="${app.title} icon" loading="lazy" />
            <div class="app-title-area-new">
              ${badgeHtml}
              <h3 class="app-title">${app.title}</h3>
              <span class="category-tag">${capitalizeFirst(app.category)}</span>
            </div>
          </div>
          <div class="app-card-content-new">
            <p class="app-desc">${app.shortDesc}</p>
            <div class="app-card-footer-new">
              <a href="${app.playStoreUrl}" target="_blank" rel="noopener noreferrer" class="view-details-btn">
                View Details
                <svg width="16" height="16" viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="2" stroke-linecap="round" stroke-linejoin="round">
                  <line x1="5" y1="12" x2="19" y2="12"></line>
                  <polyline points="12 5 19 12 12 19"></polyline>
                </svg>
              </a>
            </div>
          </div>
        </div>
      `;

      // Redirect on card click (excluding direct link click)
      card.addEventListener("click", (e) => {
        if (!e.target.closest(".view-details-btn")) {
          window.open(app.playStoreUrl, "_blank", "noopener,noreferrer");
        }
      });

      appsGrid.appendChild(card);
    });
  }

  function capitalizeFirst(str) {
    const labels = {
      puzzle: "Puzzle Games",
      board: "Board Games",
      casual: "Casual Games",
      arcade: "Arcade Games",
      adventure: "Adventure Games",
      education: "Educational Apps",
      productivity: "Productivity Apps",
      finance: "Finance Apps"
    };
    return labels[str] || (str.charAt(0).toUpperCase() + str.slice(1));
  }

  // Filter Buttons Actions
  tabBtns.forEach(btn => {
    btn.addEventListener("click", () => {
      tabBtns.forEach(b => b.classList.remove("active"));
      btn.classList.add("active");
      activeCategory = btn.getAttribute("data-category");
      renderApps(activeCategory);
      
      // Smooth scroll target showcase header if not in viewport
      const showcaseSection = document.getElementById("apps");
      if (showcaseSection) {
        const rect = showcaseSection.getBoundingClientRect();
        if (rect.top < 0) {
          showcaseSection.scrollIntoView({ behavior: "smooth" });
        }
      }
    });
  });

  // Theme Toggler
  function initTheme() {
    const savedTheme = localStorage.getItem("theme");
    const systemPrefersDark = window.matchMedia("(prefers-color-scheme: dark)").matches;
    
    if (savedTheme === "dark" || (!savedTheme && systemPrefersDark)) {
      document.documentElement.classList.add("dark");
    } else {
      document.documentElement.classList.remove("dark");
    }
  }

  themeToggle.addEventListener("click", () => {
    document.documentElement.classList.toggle("dark");
    const isDark = document.documentElement.classList.contains("dark");
    localStorage.setItem("theme", isDark ? "dark" : "light");
  });

  // Init Theme and Apps
  initTheme();
  renderApps("all");
});