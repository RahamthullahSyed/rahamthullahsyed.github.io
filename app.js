// Initialize Portfolio Website
document.addEventListener("DOMContentLoaded", () => {
  const appsGrid = document.getElementById("apps-grid");
  const tabBtns = document.querySelectorAll(".tab-btn");
  const themeToggle = document.getElementById("theme-toggle");
  const scrollToTopBtn = document.getElementById("scroll-to-top");
  const searchInput = document.getElementById("search-input");
  const searchClearBtn = document.getElementById("search-clear-btn");
  const searchForm = document.getElementById("search-form");

  let activeCategory = "all";

  // Escape HTML helper to prevent XSS issues when rendering user search query
  function escapeHtml(string) {
    return String(string).replace(/[&<>"']/g, function (s) {
      return {
        '&': '&amp;',
        '<': '&lt;',
        '>': '&gt;',
        '"': '&quot;',
        "'": '&#39;'
      }[s];
    });
  }

  // Render Apps list
  function renderApps(category = "all") {
    appsGrid.innerHTML = "";
    
    const query = searchInput ? searchInput.value.toLowerCase().trim() : "";
    
    // Toggle search clear button visibility
    if (searchClearBtn) {
      if (query.length > 0) {
        searchClearBtn.classList.add("show");
      } else {
        searchClearBtn.classList.remove("show");
      }
    }

    const filteredApps = appsData.filter(app => {
      const matchesCategory = category === "all" || app.category === category;
      const matchesSearch = !query || 
        app.title.toLowerCase().includes(query) ||
        app.shortDesc.toLowerCase().includes(query) ||
        app.category.toLowerCase().includes(query) ||
        (app.features && app.features.some(f => f.toLowerCase().includes(query)));
      return matchesCategory && matchesSearch;
    });

    if (filteredApps.length === 0) {
      if (query) {
        appsGrid.innerHTML = `
          <div class="col-span-full no-results-container">
            <div class="no-results-icon">
              <svg width="48" height="48" viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="1.5" stroke-linecap="round" stroke-linejoin="round">
                <circle cx="11" cy="11" r="8"></circle>
                <line x1="21" y1="21" x2="16.65" y2="16.65"></line>
                <line x1="8" y1="11" x2="14" y2="11"></line>
              </svg>
            </div>
            <h3 class="no-results-title">No apps match your search</h3>
            <p class="no-results-text">We couldn't find any apps matching "${escapeHtml(query)}". Try checking your spelling or adjusting your filters.</p>
          </div>
        `;
      } else {
        appsGrid.innerHTML = `
          <div class="col-span-full text-center py-16 text-gray-500">
            <p class="text-lg font-medium">No apps found in this category.</p>
          </div>
        `;
      }
      return;
    }

    filteredApps.forEach(app => {
      const card = document.createElement("div");
      card.className = "app-card group opacity-0 translate-y-4";
      card.style.animation = "fadeInUp 0.6s forwards cubic-bezier(0.16, 1, 0.3, 1)";
      
      const badgeHtml = app.badge 
        ? `<span class="app-badge bg-gradient-to-r ${app.color} text-white">${app.badge}</span>` 
        : "";

      // Feature graphic banner HTML
      const bannerHtml = app.featureGraphicUrl 
        ? `<div class="app-card-banner" style="background-image: url('${app.featureGraphicUrl}');"></div>`
        : `<div class="app-card-banner bg-gradient-to-br ${app.color}"></div>`;

      card.innerHTML = `
        ${bannerHtml}
        <div class="app-card-inner-new">
          <div class="app-card-header-new">
            <img class="app-icon-new" src="${app.iconUrl}" alt="${app.title} icon" loading="lazy" />
            <div class="app-title-area-new">
              ${badgeHtml}
              <h3 class="app-title">${app.title}</h3>
              <span class="category-tag">${capitalizeFirst(app.category)}</span>
            </div>
          </div>
          <div class="app-card-content-new">
            <p class="app-desc">${app.shortDesc}</p>
            <div class="app-card-footer-new">
              <a href="${app.playStoreUrl}" target="_blank" rel="noopener noreferrer" class="view-details-btn">
                View Details
                <svg width="16" height="16" viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="2" stroke-linecap="round" stroke-linejoin="round">
                  <line x1="5" y1="12" x2="19" y2="12"></line>
                  <polyline points="12 5 19 12 12 19"></polyline>
                </svg>
              </a>
            </div>
          </div>
        </div>
      `;

      // Redirect on card click (excluding direct link click)
      card.addEventListener("click", (e) => {
        if (!e.target.closest(".view-details-btn")) {
          window.open(app.playStoreUrl, "_blank", "noopener,noreferrer");
        }
      });

      appsGrid.appendChild(card);
    });
  }

  function capitalizeFirst(str) {
    const labels = {
      puzzle: "Puzzle Games",
      board: "Board Games",
      casual: "Casual Games",
      arcade: "Arcade Games",
      adventure: "Adventure Games",
      education: "Educational Apps",
      productivity: "Productivity Apps",
      finance: "Finance Apps"
    };
    return labels[str] || (str.charAt(0).toUpperCase() + str.slice(1));
  }

  // Filter Buttons Actions
  tabBtns.forEach(btn => {
    btn.addEventListener("click", () => {
      tabBtns.forEach(b => b.classList.remove("active"));
      btn.classList.add("active");
      activeCategory = btn.getAttribute("data-category");
      renderApps(activeCategory);
      
      // Smooth scroll target showcase header if not in viewport
      const showcaseSection = document.getElementById("apps");
      if (showcaseSection) {
        const rect = showcaseSection.getBoundingClientRect();
        if (rect.top < 0) {
          showcaseSection.scrollIntoView({ behavior: "smooth" });
        }
      }
    });
  });

  // Search Form Submit Handler (prevents page reload on enter and triggers search)
  if (searchForm) {
    searchForm.addEventListener("submit", (e) => {
      e.preventDefault();
      renderApps(activeCategory);
    });
  }

  // Search Input Handler
  if (searchInput) {
    searchInput.addEventListener("input", () => {
      renderApps(activeCategory);
    });
  }

  // Clear Search Action
  if (searchClearBtn) {
    searchClearBtn.addEventListener("click", () => {
      searchInput.value = "";
      renderApps(activeCategory);
      searchInput.focus();
    });
  }

  // Theme Toggler
  function initTheme() {
    const savedTheme = localStorage.getItem("theme");
    const systemPrefersDark = window.matchMedia("(prefers-color-scheme: dark)").matches;
    
    if (savedTheme === "dark" || (!savedTheme && systemPrefersDark)) {
      document.documentElement.classList.add("dark");
    } else {
      document.documentElement.classList.remove("dark");
    }
  }

  themeToggle.addEventListener("click", () => {
    document.documentElement.classList.toggle("dark");
    const isDark = document.documentElement.classList.contains("dark");
    localStorage.setItem("theme", isDark ? "dark" : "light");
  });

  // Scroll Spy for Navigation Links
  const sections = document.querySelectorAll("section[id]");
  const navLinks = document.querySelectorAll(".nav-links a.nav-link");

  function scrollSpy() {
    let currentSection = "";
    
    // Check if scrolled to the very bottom of the page
    const isAtBottom = (window.innerHeight + window.scrollY) >= document.documentElement.scrollHeight - 50;
    
    if (isAtBottom) {
      currentSection = "contact";
    } else {
      sections.forEach(section => {
        const rect = section.getBoundingClientRect();
        if (rect.top <= 160 && rect.bottom >= 160) {
          currentSection = section.getAttribute("id");
        }
      });
    }

    navLinks.forEach(link => {
      link.classList.remove("active");
      const href = link.getAttribute("href");
      if (href === `#${currentSection}` || (currentSection === "" && href === "#about")) {
        link.classList.add("active");
      }
    });
  }

  window.addEventListener("scroll", scrollSpy);

  // Scroll to Top Interaction
  const handleScroll = () => {
    const scrollTop = window.pageYOffset || document.documentElement.scrollTop || window.scrollY || 0;
    if (scrollTop > 300) {
      scrollToTopBtn.classList.add("show");
    } else {
      scrollToTopBtn.classList.remove("show");
    }
  };

  window.addEventListener("scroll", handleScroll, { passive: true });
  window.addEventListener("touchmove", handleScroll, { passive: true });

  scrollToTopBtn.addEventListener("click", () => {
    window.scrollTo({
      top: 0,
      behavior: "smooth"
    });
  });

  // Init Theme, Scroll Spy, and Apps
  initTheme();
  scrollSpy();
  renderApps("all");
});